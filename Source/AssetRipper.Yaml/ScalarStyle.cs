namespace AssetRipper.Yaml;

/// <summary>
/// Specifies the style of a Yaml scalar.
/// </summary>
public enum ScalarStyle
{
	/// <summary>
	/// The plain scalar style.
	/// </summary>
	Plain,

	/// <summary>
	/// The single-quoted scalar style.
	/// </summary>
	SingleQuoted,

	/// <summary>
	/// The double-quoted scalar style.
	/// </summary>
	DoubleQuoted,
}
